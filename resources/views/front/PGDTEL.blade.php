			  <h5>
			  <em>Tourism industry is one of the most significant industries globally, despite the ups and downs in world economy throughout the recent years. There is a vast untapped potential inherent in the industry for generating more wealth, income and employment. Not only is the tourism sector a generator of local, national and global income but is can also be considered the backbone for allied sectors, like hospitality, civil aviation, and transport.</em>
			  </h5>
            
              <p>Despite having such potential, tourism remains one of the largest unorganised sectors in India creating the possibility of enhancing the negative impacts of tourism on society, culture and the environment. Hence it is essential to efficiently plan, manage and promote tourism while striking a suitable balance between the social, cultural and environmental aspects of tourism development, so as to ensure its sustainability.</p>
              <p>In this context, The Centre for Environmental Law (CEL), WWF-India along with the National Law University, Delhi (NLU, Delhi) is launching a Post Graduate Diploma in Tourism and Environmental Law (PGDTEL) in September 2012. WWF India and NLU, Delhi are already jointly offering a Post Graduate Diploma in Urban Environmental Management and Law (PGDUEML) which was launched in August 2011. The programme was aimed towards disseminating specialised knowledge on Urban Environmental issues to a wide range of audience from various backgrounds including Architects, IAS, IPS and IFS Officers, Urban Planners and Environmental Engineers. The programme received an overwhelming response in its first batch itself where applications were received from more than a hundred candidates.</p>
			  <p>Encouraged by the success of PGDUEML, both WWF-India and NLU Delhi, have come together once again to offer this tailor-made programme to benefit all tourism providers and prospective tourism service providers. It aims to assist professionals and fresh graduates to develop a better understanding of the tourism industry, especially in relation to those working in areas of ecotourism. The programme includes perspectives on all the new developments and new insights into the relationship between tourism and the environment dealt with at both global and regional level.</p>
			  <p>The programme will highlight the crucial socio-environmental problems related to the tourism industry along with the legal and policy issues, as well as present the steps to manage and overcome the same. This exciting course will be unique as it covers the scope and nature of ecotourism in today's market and looks at the sources and types of opportunities available within this fast growing industry.</p>
			  <p>The highlight to the programme is that it will provide an interdisciplinary analysis of the current ecotourism trends with special emphasis on synthesis of tourism laws with environmental concerns. It is aimed at providing a broader perspective of environmental governance, sustainable tourism and ecotourism standpoint to varied interest groups by not only providing them with an in-depth knowledge and understanding of the existing issues of sustainable tourism, but also shall train the tourism professionals to optimally utilize financial and technical resources/materials to develop a sustainable, step by step plan of their own ecotourism product.
PGDTEL is a one year programme that shall be offered through both ‘Regular’ and ‘Online’ modes. The medium of instruction shall be in English. Interested candidates can opt for any one of the two instructional modes for pursuing the programme at the time of admission.</p>
			 
			  
